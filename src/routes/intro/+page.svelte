    <h1>Introduction</h1>
    <p>
      <span font-bold>Serge Toubiana and Frédéric Strauss:</span> In most of your films, there seems
      to be a sense of melancholy for the past. But the two children, who are not subject to this melancholy,
      are pulling you in a different direction.
    </p>
    <p>
      <span font-bold>Theodoros Angelopoulos</span>: I believe the past is my own personal past
      dragged into the present by my occupation as a filmmaker. The tree at the end of the film is
      the tree from Voyage to Cythera, a reference to my own personal film landscape. In the course
      of this picture, the children cross a film landscape in order to reach, at the end, a
      different film landscape, which, I believe, should offer them renewed hope. I would like to
      believe the world will be saved by the cinema. Cinema is my world and it is the scope of all
      my journeys. I am always searching for secret little utopias that will enchant me; I am doing
      my best to believe in the relevance of these trips I am constantly embarking on through my
      films.
    </p>
    <footer border="t-2 primary" pt-2>
