	<h3>Speaking &amp; Organising</h3>

	<p>
		Born in a small town in China and later moving to Canada, I grew up facing oppression for my
		dissident, queer, and disabled identities. Over time, I learned to transform personal struggles
		into community activism and solidarity, On the <a
			href="https://insightmyanmar.org/complete-shows/2025/10/19/episode-418-super-rabbit-person"
			rel="external"
			target="_blank">Insight Myanmar Podcast</a
		>, I shared my stories and insights as a youth human rights activist.
	</p>

	<p>
		Since middle school period in China, I spoke out anonymously for Hong Kong’s democracy and
		freedom. After moving to Canada in 2022, I joined the organisation Students for Hong Kong,
		advocating publicly as a speaker and community organiser for rallies and community workshops. I
		also engaged in online advocacy, gaining over 10,000 followers on X (formerly Twitter) in 2023
		before deleting my account for safety reasons.
	</p>

	<p>
		I now view diaspora struggles and other social issues, as intersectional issues shaped by the
		context of global capitalism. I collaborate with organisations and activists worldwide and work
		on projects such as fundraising for Gaza, community education programs, and other initiatives
		supporting oppressed communities.
	</p>

