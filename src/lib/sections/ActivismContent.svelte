	<h3>2026 International Women’s Day</h3>
	<p>Happy International Women’s Day!</p>
	<p>
		Back when I graduated from high school and came to Canada in September 2022, “Women Life
		Freedom” movement was the movement I participated in the earliest. I later connected it with the
		A4 Revolution after the Ürümqi fire in China, which happened the same year, and which also
		resonated most with my diaspora identity.
	</p>
	<p>
		This spring, I completed my undergraduate thesis, in which I focused on digital feminist
		activism following the Nth Room case in South Korea. Korean feminist movements have long been
		another encouragement for me.
	</p>
	<p>
		Therefore, I want to once again emphasise transnational solidarity among women, femmes, and
		diasporas.
	</p>
	<p>
		Throughout my university life, I am so grateful to meet more women, femmes, and queer people
		from everywhere, inspiring me to continue my feminist work. My identity as a woman is not only
		assigned by birth but also adopted and chosen by myself as a part of womanhood.
	</p>

	<p>
		At the same time, as a non-binary woman who goes by they/them, I understand the struggles of
		masculine women, trans women, non-binary women, and any women or femmes who do not conform to
		social expectations. Especially in today’s intense times, when extremist politics seek to divide
		us, I want to again state that I will always stand with the trans community. Trans women are
		women—it is also Trans Women’s Day.
	</p>

	<p>
		I also want to express solidarity with Muslim women, Black women, and other underrepresented
		women’s communities. As Audre Lorde says, “I am not free while any woman is unfree, even when
		her shackles are very different from my own,” I will always speak up for all the oppressed.
	</p>
	<p>
		From Hongkongese and Chinese women to Palestinian and Iranian women, in solidarity with all.
	</p>

	<h3>2025 Pride Month</h3>
	<p>
		This Pride Month, I want to amplify trans community to celebrate the strength of our solidarity
		in a time of rising hatred. This hatred is not only embodied the Trump-style right-wing
		politics, but also by the global trend of separatist ideologies that try to divide us,
		positioning LGB and women’s rights against trans existence, and fragmenting the whole queer
		community.
	</p>
	<p></p>
	<p>
		We’ve also witnessed how governments, banks, and fossil fuel and mining corporations have become
		complicit in hijacking our identities and histories, using them to justify colonialism in
		Palestine, Congo, and Sudan; against First Nations, migrants, and refugees.
	</p>
	<p>
		An imperial regime wages war and aggravates climate injustice by dropping bombs and poisoning
		the clean water. A xenophobic nation that marginalises migrant and refugees is the ableist
		society that divides people into different classes and forces disabled people to fit a single,
		violent norm.
	</p>
	<p>We live with different identities, but we are all struggling in the same world.</p>
	<p>
		I remember the first activity I participated in after coming to Canada — it was a trans rally in
		2022. I had just arrived from China, where activism is illegal, so it was my first time
		experiencing freedom. I appreciate the freedom and have continued to show up, whether as a
		public speaker or working behind the scenes.
	</p>
	<p>
		Now, I’m watching the government in my home country criminalise queer writers for writing BL
		novels, while the Hong Kong democracy I’ve been advocating for years is being eroded again, as
		one of its last pro-democracy parties disbands under political pressure. And here, we’re all
		facing Bill C-5 in Canada and ICE in the US. Our freedom is under threat, so I’m here again — to
		call out the violence.
	</p>
	<p>
		Pride has always been resistance against injustice. It’s not a tool to justify pinkwashing. My
		queerness is always tied with my identity as disabled, diaspora, and dissident. This
		intersection motivates me to stand in solidarity with justice and liberation for all.
	</p>
	<p>
		When an era forces us to deny our existence, we become the generation that stops injustice
		through resistance. We are the stonewall rebellion of our time.
	</p>
